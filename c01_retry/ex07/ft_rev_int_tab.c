void	ft_rev_int_tab(int *tab, int size)
{
	int temp;
	int i;

	temp = 0;
	i = 0;
	size = size - 1;
	while (i < size)
	{
		temp = tab[i];
		tab[i] = tab[size];
		tab[size] = temp;
		i++;
		size--;
	}
}
